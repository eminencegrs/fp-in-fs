namespace FPinFSharp.Exercises.Chapter_02

// 2.2 Operator precedence and association
module Section_02_02 =

    let add a b =
        a + b

    let subtract a b =
        a - b

    let multiply x y =
        x * y

    let divide x y =
        x / y

    let modulo x y =
        x % y

    let exponentiate x y =
        x ** y
